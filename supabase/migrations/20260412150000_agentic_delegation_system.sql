  CHECK (event_type IN (
    'message_sent', 'message_responded',
    'proactive_accepted', 'proactive_ignored', 'proactive_rejected',
    'action_approved', 'action_rejected',
    'task_completed', 'note_created', 'session_start',
    -- New Sprint 5 event types
    'delegation_created', 'delegation_accepted', 'delegation_declined',
    'delegation_completed', 'delegation_snoozed', 'delegation_reassigned',
    'briefing_read'
  ));
